    private void editar() {
        DefaultTableModel tblModel = (DefaultTableModel) tblAgenda.getModel();
        int i = tblAgenda.getSelectedRow();
        String idString = tblModel.getValueAt(i, 0).toString();

        if (!idString.equals("")) {
            int id = Integer.parseInt(idString);

            int op = JOptionPane.showConfirmDialog(null, ("Deseja editar o agendamento selecionado de id = " + id + " ?"));

            if (op == 0) {
                String nome = txtNome.getText().toUpperCase();
                String veiculo = txtVeiculo.getText().toUpperCase();
                String telefone = txtTelefone.getText();
                String bairro = txtBairro.getText();
                String rua = txtRua.getText();
                String numero = txtNumero.getText();

                String dataString = txtData.getText().trim();
                DateTimeFormatter formatoData = DateTimeFormatter.ofPattern("dd/MM/yyyy");
                LocalDate data = LocalDate.parse(dataString, formatoData);

                String horaString = txtHora.getText().trim();
                DateTimeFormatter formatoHora = DateTimeFormatter.ofPattern("HH:mm");
                LocalTime hora = LocalTime.parse(horaString, formatoHora);

                String precoFormatado = txtPreco.getText();
                if (precoFormatado.substring(0, 2).equals("R$")) {
                    precoFormatado = precoFormatado.substring(2).trim();
                }
                double preco = convertarPrecoFormatadoParaDouble(precoFormatado);

                verificarModificacaoNosServicos(id);

                Agendamento agendamento = new Agendamento();
                agendamento.setId(id);
                agendamento.setCliente(nome);
                agendamento.setVeiculo(veiculo);
                agendamento.setContato(telefone);
                agendamento.setBairro(bairro);
                agendamento.setRua(rua);
                agendamento.setNumero(numero);
                agendamento.setData(data);
                agendamento.setHora(hora);
                agendamento.setPreco(preco);
                agendamento.setServicosSelecionados(somaServicos());
                if (jRadioAprovado.isSelected()) {
                    agendamento.setPagamento(true);
                } else if (jRadioPendente.isSelected()) {
                    agendamento.setPagamento(false);
                }

                String funcionarioResponsavel = jcbFuncionarioResponsavel.getSelectedItem().toString();
                FuncionarioDAO funcionarioDAO = new FuncionarioDAO();
                Funcionario funcionario = funcionarioDAO.retornaFuncionarioNome(funcionarioResponsavel);
                agendamento.setIdFuncionarioResponsavel(funcionario.getId());

                AgendamentoDAO agendamentoDAO = new AgendamentoDAO();
                agendamentoDAO.editar(agendamento);
                txtConfirmacao.setText("Agendamento editado com sucesso.");
                preencheTabelaAgendamentos();
                limparCampos();
            }
        }

    }

    public double convertarPrecoFormatadoParaDouble(String valorFormatado) {
        DecimalFormatSymbols symbols = new DecimalFormatSymbols(Locale.getDefault());
        char decimalSeparator = symbols.getDecimalSeparator();

        // Removendo o separador de milhar, caso exista
        valorFormatado = valorFormatado.replace(Character.toString(symbols.getGroupingSeparator()), "");

        // Substituindo a vírgula pelo ponto
        valorFormatado = valorFormatado.replace(Character.toString(decimalSeparator), ".");

        double valorDouble = Double.parseDouble(valorFormatado);
        return valorDouble;
    }


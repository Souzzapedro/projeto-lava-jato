    public void cadastrarDispesa() {
        String titulo = txtTitulo.getText().toUpperCase();
        double preco = Double.parseDouble(txtPreco.getText());
        
        Dispesa dispesa = new Dispesa(titulo, preco);
        
        if (dispesa.getTitulo().equals("") || dispesa.getPreco() == 0) {
            txtErro.setText("Por favor, preencha os campos");
        } else {

           DispesaDAO dispesaDAO = new DispesaDAO();
           dispesaDAO.incluir(dispesa);
           
           FinancasView financasView = new FinancasView();
           financasView.dispose();
           financasView.setVisible(true);
           dispose();

        }
    }

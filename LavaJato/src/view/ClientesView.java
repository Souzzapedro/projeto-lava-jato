    private void preencheTabelaClientes() {
        ClienteDAO clienteDAO = new ClienteDAO();
        String pesquisa = txtPesquisarClientes.getText().toUpperCase();
        
        List<Cliente> listaClientes = clienteDAO.retornaPesquisaNome(pesquisa);

        System.out.println("entrou ne preencheTabelaClientes");
        DefaultTableModel tabelaClientes = (DefaultTableModel) tblClientes.getModel();
        tabelaClientes.setRowCount(0);

        for (Cliente c : listaClientes) {
            Object[] obj = new Object[]{
                c.getId(),
                c.getNome(),
                c.getVeiculo(),
                c.getTelefone()
            };
            tabelaClientes.addRow(obj);
        }

        txtUserLogado.setText(funcionarioLogado.getNome());
    }

    public void RetornaLinhaTabela() {
        DefaultTableModel tblModel = (DefaultTableModel) tblClientes.getModel();
        int i = tblClientes.getSelectedRow();
        String id = tblModel.getValueAt(i, 0).toString();

        int idPesquisa = Integer.parseInt(id);
        ClienteDAO clienteDAO = new ClienteDAO();
        Cliente cliente = clienteDAO.retornaPeloId(idPesquisa);

        txtNome.setText(cliente.getNome());
        txtVeiculo.setText(cliente.getVeiculo());
        txtBairro.setText(cliente.getBairro());
        txtRua.setText(cliente.getRua());
        txtNumero.setText(cliente.getNumero());
        txtTelefone.setText(cliente.getTelefone());
        txtConfirmacao.setText("");
    }

    public void remover() {
        DefaultTableModel tblModel = (DefaultTableModel) tblClientes.getModel();
        int i = tblClientes.getSelectedRow();
        String id = tblModel.getValueAt(i, 0).toString();

        int idPesquisa = Integer.parseInt(id);
        int op = JOptionPane.showConfirmDialog(null, ("Deseja remover o cliente selecionado de id " + idPesquisa));
        if (op == 0) {
            ClienteDAO clienteDAO = new ClienteDAO();
            clienteDAO.remover(idPesquisa);
            preencheTabelaClientes();
            limpaCampos();
        }
    }

    public void editar() {
        DefaultTableModel tblModel = (DefaultTableModel) tblClientes.getModel();
        int i = tblClientes.getSelectedRow();
        String idString = tblModel.getValueAt(i, 0).toString();

        if (!idString.equals("")) {
            int id = Integer.parseInt(idString);

            int op = JOptionPane.showConfirmDialog(null, ("Deseja editar o cliente de id = " + id + " ?"));

            if (op == 0) {
                String nome = txtNome.getText().toUpperCase();
                String veiculo = txtVeiculo.getText().toUpperCase();
                String telefone = txtTelefone.getText();
                String bairro = txtBairro.getText();
                String rua = txtRua.getText();
                String numero = txtNumero.getText();

                Cliente cliente = new Cliente();
                cliente.setId(id);
                cliente.setNome(nome);
                cliente.setBairro(bairro);
                cliente.setRua(rua);
                cliente.setNumero(numero);
                cliente.setTelefone(telefone);
                cliente.setVeiculo(veiculo);

                ClienteDAO clienteDAO = new ClienteDAO();
                clienteDAO.editar(cliente);
                limpaCampos();
                preencheTabelaClientes();
                limpaCampos();
            }
        }
    }
    
    public void limpaCampos(){
        txtNome.setText("");
        txtVeiculo.setText("");
        txtTelefone.setText("");
        txtBairro.setText("");
        txtRua.setText("");
        txtNumero.setText("");
        txtPesquisarClientes.setText("");
    }

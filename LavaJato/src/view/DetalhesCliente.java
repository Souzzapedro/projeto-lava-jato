     public void preencheTabelaDetalhes() {
         System.out.println(idFuncionario);
        AgendamentoDAO agendamentoDAO = new AgendamentoDAO();
        List<Agendamento> listaAgendamentos = agendamentoDAO.getAgendamentosPosFuncionario(this.idFuncionario);

        DecimalFormat format = new DecimalFormat("#,##0.00");
        DefaultTableModel tabelaAgendamentos = (DefaultTableModel) tblDetalhes.getModel();
        tabelaAgendamentos.setRowCount(0);

        for (Agendamento a : listaAgendamentos) {
            String pagamento;
            if (a.isPagamento()) {
                pagamento = "APROVADO";
            } else {
                pagamento = "PENDENTE";
            }

            Object[] obj = new Object[]{
                a.getId(),
                a.getCliente(),
                a.getVeiculo(),
                a.getData(),
                a.getHora(),
                format.format(a.getPreco()),
                pagamento
            };
            tabelaAgendamentos.addRow(obj);
        }

    }
     
     public void RetornaLinhaTabela() {
        jtbLavagem.setSelected(false);
        jtbCera.setSelected(false);
        jtbPolimento.setSelected(false);
        jtbRestauracaoPlasticos.setSelected(false);
        jtbRestauracaoFarois.setSelected(false);
        jtbLavagemBancos.setSelected(false);
        jtbAromatizante.setSelected(false);
        DefaultTableModel tblModel = (DefaultTableModel) tblDetalhes.getModel();
        int i = tblDetalhes.getSelectedRow();
        String id = tblModel.getValueAt(i, 0).toString();

        int idPesquisa = Integer.parseInt(id);
        AgendamentoDAO agendamentoDAO = new AgendamentoDAO();
        Agendamento agendamento = agendamentoDAO.getAgendamento(idPesquisa);

        if (agendamento.getServicosSelecionados().isLavagem()) 
            jtbLavagem.setSelected(true);

        if (agendamento.getServicosSelecionados().isCera()) 
            jtbCera.setSelected(true);

        if (agendamento.getServicosSelecionados().isPolimento()) 
            jtbPolimento.setSelected(true);

        if (agendamento.getServicosSelecionados().isRestauracaoPlasticos()) 
            jtbRestauracaoPlasticos.setSelected(true);

        if (agendamento.getServicosSelecionados().isRestauracaoFarois()) 
            jtbRestauracaoFarois.setSelected(true);      

        if (agendamento.getServicosSelecionados().isLavagemBancos())
            jtbLavagemBancos.setSelected(true);
       
        if (agendamento.getServicosSelecionados().isAromatizante()) 
            jtbAromatizante.setSelected(true);
        
    }
     

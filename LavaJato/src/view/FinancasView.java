    private void calcularTudo() {
        DecimalFormat format = new DecimalFormat("R$ #,##0.00");
        AgendamentoDAO agendamentoDAO = new AgendamentoDAO();

        double lucroBruto = agendamentoDAO.somarLucroBruto();
        txtLucroBruto.setText(format.format(lucroBruto));

        List<Agendamento> agendamentosPagos = agendamentoDAO.getServicosPagos();
        txtDispesasProdutos.setText(format.format(calcularGastoprodutos(agendamentosPagos)));
                
    }


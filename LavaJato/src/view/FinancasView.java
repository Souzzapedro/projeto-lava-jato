    private void preencheTabela() {
        DispesaDAO dispesaDAO = new DispesaDAO();
        String pesquisa = txtPesquisar.getText().toUpperCase();

        List<Dispesa> listaDispesas = dispesaDAO.getDispesas(pesquisa);

        DefaultTableModel tabelaFinancas = (DefaultTableModel) tblFinancas.getModel();
        tabelaFinancas.setRowCount(0);

        for (Dispesa d : listaDispesas) {

            DecimalFormat formato = new DecimalFormat("R$ #,##0.00");
            String precoFormatado = (formato.format(d.getPreco()));

            Object[] obj = new Object[]{
                d.getId(),
                d.getTitulo(),
                precoFormatado
            };
            tabelaFinancas.addRow(obj);
        }

        txtUserLogado.setText(funcionarioLogado.getNome());
    }

    private double calcularGastoprodutos(List<Agendamento> agendamentos) {
        double soma = 0.0;

        for (Agendamento a : agendamentos) {

            EstoqueView estoqueView = new EstoqueView();
            if (a.getServicosSelecionados().isLavagem()) {
                soma += estoqueView.somarDispesa("SHAMPOO");
                soma += estoqueView.somarDispesa("LIMPA-BAU");
                soma += estoqueView.somarDispesa("DESENGRAXANTE");
                soma += estoqueView.somarDispesa("PRETINHO");
            }

            if (a.getServicosSelecionados().isCera()) {
                soma += estoqueView.somarDispesa("CERA");
            }

            if (a.getServicosSelecionados().isPolimento()) {
                soma += estoqueView.somarDispesa("MASSSA-DE-POLIR");
                soma += estoqueView.somarDispesa("CERA");
            }

            if (a.getServicosSelecionados().isRestauracaoPlasticos()) {
                soma += estoqueView.somarDispesa("REVITALIZADOR-DE-PLASTICOS");
            }

            if (a.getServicosSelecionados().isRestauracaoFarois()) {
                soma += estoqueView.somarDispesa("MASSA-DE-POLIR-FAROIS");
            }

            if (a.getServicosSelecionados().isLavagemBancos()) {
                soma += estoqueView.somarDispesa("SHAMPOO");
            }

            if (a.getServicosSelecionados().isAromatizante()) {
                soma += estoqueView.somarDispesa("AROMATIZANTE");
            }
        }
        return soma;
    }

    public void preencheTabelaFuncionarios(boolean comFiltro) {
        String dataInicialString = "";
        String dataFinalString = "";

        if (comFiltro) {
            if (validarFormatoData(txtDataInicial.getText())) {
                dataInicialString = txtDataInicial.getText();
            } else {
                txtConfirmacao.setText("Data inválida");
                return;
            }
            if (validarFormatoData(txtDataFinal.getText())) {
                dataFinalString = txtDataFinal.getText();
            } else {
                txtConfirmacao.setText("Data inválida");
                return;
            }
        }

        FuncionarioDAO funcionarioDAO = new FuncionarioDAO();
        String pesquisa = txtPesquisarFuncionario.getText().toUpperCase();

        List<Funcionario> listaFuncionarios = funcionarioDAO.getFuncionarios(pesquisa);

        DefaultTableModel tabelaFuncionarios = (DefaultTableModel) tblFuncionarios.getModel();
        tabelaFuncionarios.setRowCount(0);

        AgendamentoDAO agendamentoDAO = new AgendamentoDAO();

        for (Funcionario a : listaFuncionarios) {
            double comissao = 0.0;
            if (comFiltro) {
                DateTimeFormatter formatoData = DateTimeFormatter.ofPattern("dd/MM/yyyy");
                LocalDate dataInicial = LocalDate.parse(dataInicialString, formatoData);
                LocalDate dataFinal = LocalDate.parse(dataFinalString, formatoData);
                comissao = agendamentoDAO.calcularComissaoFuncionario(a.getId(), dataInicial, dataFinal);
            } else {
                comissao = agendamentoDAO.calcularComissaoFuncionario(a.getId());
            }
            DecimalFormat format = new DecimalFormat("R$ #,##0.00");
            Object[] obj = new Object[]{
                a.getId(),
                a.getNome(),
                a.getTelefone(),
                format.format(a.getSalario()),
                format.format(comissao)
            };
            tabelaFuncionarios.addRow(obj);
        }

        txtUserLogado.setText(funcionarioLogado.getNome());
    }

    public void RetornaLinhaTabela() {
        limparCampos();
        DecimalFormat format = new DecimalFormat("R$ #,##0.00");
        DefaultTableModel tblModel = (DefaultTableModel) tblFuncionarios.getModel();
        int i = tblFuncionarios.getSelectedRow();
        String id = tblModel.getValueAt(i, 0).toString();

        int idPesquisa = Integer.parseInt(id);
        FuncionarioDAO funcionarioDAO = new FuncionarioDAO();
        Funcionario funcionario = funcionarioDAO.getFuncionario(idPesquisa);

        txtNome.setText(funcionario.getNome());
        txtTelefone.setText(funcionario.getTelefone());
        txtCpf.setText(funcionario.getCpf());
        txtCep.setText(funcionario.getCep());
        txtBairro.setText(funcionario.getBairro());
        txtRua.setText(funcionario.getRua());
        txtNumero.setText(funcionario.getNumero());
        txtSalario.setText(format.format(funcionario.getSalario()));
        txtConfirmacao.setText("");

        AgendamentoDAO agendamentoDAO = new AgendamentoDAO();
        
        double comissao = 0.0;
        if (validarFormatoData(txtDataInicial.getText()) && validarFormatoData(txtDataFinal.getText())) {
            String dataInicialString = txtDataInicial.getText();
            String dataFinalString = txtDataFinal.getText();
            DateTimeFormatter formatoData = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            LocalDate dataInicial = LocalDate.parse(dataInicialString, formatoData);
            LocalDate dataFinal = LocalDate.parse(dataFinalString, formatoData);
            comissao = agendamentoDAO.calcularComissaoFuncionario(idPesquisa, dataInicial, dataFinal);
        }else{
            comissao = agendamentoDAO.calcularComissaoFuncionario(idPesquisa);
        }
        txtComissoes.setText(format.format(comissao));
        txtTotal.setText(format.format(funcionario.getSalario() + comissao));
    }

    public void remover() {
        int op = JOptionPane.showConfirmDialog(null, "Deseja cancelar o funcionario selecionado?");
        if (op == 0) {
            DefaultTableModel tblModel = (DefaultTableModel) tblFuncionarios.getModel();
            int i = tblFuncionarios.getSelectedRow();
            String id = tblModel.getValueAt(i, 0).toString();

            int idPesquisa = Integer.parseInt(id);
            FuncionarioDAO funcionarioDAO = new FuncionarioDAO();
            funcionarioDAO.remover(idPesquisa);

            txtConfirmacao.setText("funcionario cancelado com sucesso.");
            preencheTabelaFuncionarios(false);
        }
        limparCampos();
    }


    private void editar() {
        DefaultTableModel tblModel = (DefaultTableModel) tblFuncionarios.getModel();
        int i = tblFuncionarios.getSelectedRow();
        String idString = tblModel.getValueAt(i, 0).toString();

        if (!idString.equals("")) {
            int id = Integer.parseInt(idString);

            int op = JOptionPane.showConfirmDialog(null, ("Deseja editar o funcionario selecionado de id = " + id + " ?"));

            if (op == 0) {
                String nome = txtNome.getText().toUpperCase();
                String telefone = txtTelefone.getText();
                String bairro = txtBairro.getText();
                String rua = txtRua.getText();
                String numero = txtNumero.getText();
                String cpf = txtCpf.getText();
                String cep = txtCep.getText();
                
                String salarioFormatado = txtSalario.getText().trim();
                if (salarioFormatado.substring(0, 2).equals("R$")) {
                    salarioFormatado = salarioFormatado.substring(2);
                }
                double salario = convertarPrecoFormatadoParaDouble(salarioFormatado);
                
                String comissoesFormatado = txtComissoes.getText().trim();
                if (comissoesFormatado.substring(0, 2).equals("R$")) {
                    comissoesFormatado = comissoesFormatado.substring(2);
                }
                double comissoes = convertarPrecoFormatadoParaDouble(comissoesFormatado);

                Funcionario funcionario = new Funcionario();
                funcionario.setId(id);
                funcionario.setNome(nome);
                funcionario.setTelefone(telefone);
                funcionario.setCpf(cpf);
                funcionario.setCep(cep);
                funcionario.setBairro(bairro);
                funcionario.setRua(rua);
                funcionario.setNumero(numero);
                funcionario.setSalario(salario);
                funcionario.setComissoes(comissoes);
                //funcionario.setAcesso(acesso);

                FuncionarioDAO funcionarioDAO = new FuncionarioDAO();
                funcionarioDAO.editar(funcionario);

                System.out.println("funcionario editado com sucesso.");
                preencheTabelaFuncionarios(false);
                limparCampos();
            }
        }

    }


    public void entrar() throws SQLException{
        String user = txtUser.getText();
        String senha = txtSenha.getText();
        
        FuncionarioDAO funcionarioDAO = new FuncionarioDAO();
        funcionarioLogado = funcionarioDAO.autenticacao(user, senha);

        if(funcionarioLogado != null){
            AgendaView agendaView = new AgendaView();
            agendaView.setVisible(true);
            this.dispose();
        }else{
            //JOptionPane.showMessageDialog(null, "Senha ou usuário inválidos.");
            txtErro.setText("Senha ou usuário inválidos, tente novamente");
        }       
    }
